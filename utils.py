# Try to reach google.com (which, presumably, will never go down)
# Basically checks if the system has an internet connection
def has_internet():
    conn = httplib.HTTPConnection("www.google.com", timeout=5)
    try:
        conn.request("HEAD", "/")
        conn.close()
        return True
    except:
        conn.close()
        return False